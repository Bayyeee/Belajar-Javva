package TipeData.nonPrimitif;

public class tipeDataNonPrimitif {
    public static void main(String[] args) {
        // ini adalah contoh dari Tipe data Non Primitif
        Integer iniInteger = 100; // variabel yang sudah di inisialisasi
        Long iniLong = 1000L;

        // ini adalah contoh tipe data yang belum di inisialisasi
        Byte iniByte = null; // masih belum di inisialisasi tapi diisikan dengan value kosong
        System.out.println(iniByte);

        // ini contoh VARIABEL YANG SUDAH DI INISIALISASI
        iniByte = 100;
        System.out.println(iniByte);
    }
}
