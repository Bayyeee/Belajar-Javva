package TipeData.primitif;

public class tipeDatakarakter {
    public static void main(String[] args) {
        // tipe data char
        char U = 'U';
        char D = 'D';
        char I = 'I';
        char N = 'N';

        System.out.print(U);
        System.out.print(D);
        System.out.print(I);
        System.out.print(N + "\n");
        System.out.printf("Nama saya %c%c%c%c", U,D,I,N);

    }
}
