package OperasiMatematika;

public class Pembagian {
    public static void main(String[] args) {
        // operator pembagian ditandai dengan ( / )
        int a = 20;
        int b = 10;
        int c = a / b;

        System.out.println(c);
        System.out.printf("Hasil dari pembagian %d / %d = %d\n", a, b, (a / b));
    }
}
