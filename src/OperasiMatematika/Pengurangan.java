package OperasiMatematika;

public class Pengurangan {
    public static void main(String[] args) {
        // operator pengurangan ditandai dengan ( - )
        int a = 10;
        int b = 20;
        int c = a - b;

        System.out.println(c);
        System.out.printf("Nilai dari pengurangan %d - %d = %d\n", a, b, (a - b));
    }
}
