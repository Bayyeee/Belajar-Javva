package OperasiMatematika;

public class Perkalian {

    public static void main(String[] args) {
        // operator pada perkalian diberikan tanda ( * )
        int a = 10;
        int b = 20;
        int c = a * b;
        System.out.println(c);
        System.out.printf("Hasil dari perkalian %d * %d = %d\n", a, b, (a * b));
    }
}
